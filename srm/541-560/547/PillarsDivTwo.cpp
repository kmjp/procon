	double maximalLength(vector <int> height, int w) {
		int L=height.size();
		int i,x,y;
		
		ZERO(dp);
		double mi=0;
		
		for(i=1;i<L;i++) for(x=1;x<=height[i-1];x++) {
			for(y=1;y<=height[i];y++) {
				dp[i][y] = max(dp[i][y], dp[i-1][x] + sqrt(w*(double)w+(y-x)*(double)(y-x)));
				if(i==L-1) mi=max(mi,dp[i][y]);
			}
		}
		return mi;
		
		
		
	}
	
