int H,W,K;
const ll mo=998244353;
ll dp[202020];

ll modpow(ll a, ll n = mo-2) {
	ll r=1;a%=mo;
	while(n) r=r*((n%2)?a:1)%mo,a=a*a%mo,n>>=1;
	return r;
}

void solve() {
	int i,j,k,l,r,x,y; string s;
	
	cin>>H>>W>>K;
	
	ll ret=0;
	if(H==1) {
		ret=modpow(K,W);
	}
	else if(W==1) {
		ret=modpow(K,H);
	}
	else {
		for(i=1;i<=K;i++) {
			dp[i]=modpow(i,H);
		}
		for(i=K;i>=1;i--) {
			(dp[i]+=mo-dp[i-1])%=mo;
			(ret+=dp[i]*modpow(K+1-i,W))%=mo;
		}
	}
	
	
	cout<<ret<<endl;
	
	
}


