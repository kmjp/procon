void solve() {
	int i,j,k,l,r,x,y; string s;
	
	cin>>T;
	while(T--) {
		cin>>N>>X;
		ll mi=N*(N+1)/2;
		if(mi>X) {
			cout<<-1<<endl;
			continue;
		}
		FOR(i,N) {
			ll mi=1LL<<60;
			for(j=59;j>=0;j--) {
				__int128 can=mi-(1LL<<j);
				if(can<N-i) continue;
				__int128 sum=(N-i)*(can+can-(N-1-i))/2;
				if(sum>=X) mi=can;
			}
			X-=mi;
			cout<<mi<<" ";
		}
		cout<<endl;
	}
}


