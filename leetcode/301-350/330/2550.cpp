const ll mo=1000000007;
ll modpow(ll a, ll n = mo-2) {
	ll r=1;a%=mo;
	while(n) r=r*((n%2)?a:1)%mo,a=a*a%mo,n>>=1;
	return r;
}


class Solution {
public:
    int monkeyMove(int n) {
        ll a=modpow(2,n)+mo-2;
        return a%mo;
    }
};
