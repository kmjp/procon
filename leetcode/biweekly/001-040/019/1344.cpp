class Solution {
public:
    double angleClock(int hour, int minutes) {
		if(hour==12) hour=0;
		double X=(hour*60+minutes)*0.5;
		double Y=minutes*6;
		return min(abs(X-Y),360-abs(X-Y));
    }
};

