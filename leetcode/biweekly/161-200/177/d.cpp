const ll mo=1000000007;

ll modpow(ll a, ll n = mo-2) {
	ll r=1;a%=mo;
	while(n) r=r*((n%2)?a:1)%mo,a=a*a%mo,n>>=1;
	return r;
}


class Solution {
public:
    int sumOfNumbers(int l, int r, int k) {
		ll a=0;
		int i;
		for(i=l;i<=r;i++) a+=i;
		a=a*modpow(r-l+1,k-1)%mo;
		ll b=(modpow(10,k)-1)*modpow(9)%mo;
		return a*b%mo;
        
    }
};
