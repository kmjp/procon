class LUPrefix {
public:
	set<int> S;
    LUPrefix(int n) {
        S.clear();
        int i;
        for(i=1;i<=n+1;i++) S.insert(i);
    }
    
    void upload(int video) {
        S.erase(video);
    }
    
    int longest() {
        return *S.begin()-1;
    }
};

