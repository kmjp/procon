class Solution {
public:
    bool canMakeSubsequence(string str1, string str2) {
		int cur=0;
		FORR(c,str1) {
			char d=c+1;
			if(c=='z') d='a';
			if(str2[cur]==c||str2[cur]==d) cur++;
			if(cur==str2.size()) return 1;
			
		}
        return 0;
    }
};
