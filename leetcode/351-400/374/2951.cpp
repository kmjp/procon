class Solution {
public:
    vector<int> findPeaks(vector<int>& mountain) {
		vector<int> R;
		int i;
		int N=mountain.size();
		for(i=1;i<N-1;i++) if(mountain[i]>mountain[i-1]&&mountain[i]>mountain[i+1]) R.push_back(i);
		return R;
        
    }
};
